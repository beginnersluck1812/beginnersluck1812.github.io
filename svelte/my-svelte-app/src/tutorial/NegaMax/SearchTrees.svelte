    <h2>Nodes</h2>
    <p>
        A <strong>node</strong> is the name given to a position in the search tree.
        <br>
        <br>
        <br>
        <iframe style="border: 1px solid rgba(0, 0, 0, 0.1);" width="800" height="450" src="https://www.figma.com/embed?embed_host=share&url=https%3A%2F%2Fwww.figma.com%2Ffile%2F7ddlZgwIdbfg25LtigCoci%2FOthello-AI-Tutorial-Designs%3Ftype%3Ddesign%26node-id%3D2477%253A124%26mode%3Ddesign%26t%3De62bwwiojYdJskHf-1" allowfullscreen></iframe>
        <br>
        <br>
        <br>
        In our search trees from here on out, I will be removing the Tic-Tac-Toe positions embedded in each node, just remember that each of those circles (nodes) represent a position.
        <br>
        <br>
        There are also different types of nodes, for example there are <strong>parent nodes</strong> and <strong>child nodes</strong>.
        <br>
        <br>
        Basically, whenever a node has positions that it can move into, the resultant positions are considered a <strong>child</strong> of the top position
        <br>
        <br>
        And the top position is considered the <strong>father</strong> of the resultant positions.
